# ── Health Event Timeline ────────────────────────────────────

class HealthEventLog(Base):
    """
    Timestamped life/health events for future
    event-health correlation analysis.

    Event types: illness, medication_change, travel,
    exam_stress, sleep_disruption, recovery_period, etc.
    """
    __tablename__ = "health_events"

    id = Column(Integer, primary_key=True, index=True)
    user_id = Column(
        Integer,
        ForeignKey("users.id", ondelete="CASCADE"),
        nullable=False,
        index=True,
    )

    event_type = Column(
        String(50), nullable=False,
        comment=(
            "illness | medication_change | travel | exam_stress | "
            "sleep_disruption | recovery_period | lifestyle_change | other"
        ),
    )
    title = Column(String(200), nullable=False)
    description = Column(Text, nullable=True)
    severity = Column(String(20), nullable=True)
    metadata_json = Column(
        JSON, nullable=True,
        comment="Flexible extra data for the event",
    )

    started_at = Column(
        DateTime(timezone=True),
        nullable=False,
        comment="When the event began",
    )
    ended_at = Column(
        DateTime(timezone=True),
        nullable=True,
        comment="When the event ended (null = ongoing)",
    )
    recorded_at = Column(
        DateTime(timezone=True),
        server_default=func.now(),
        nullable=False,
        index=True,
    )

    user = relationship("User", backref="health_events")


