"""
health_intelligence/models.py
───────────────────────────────────────────────
SQLAlchemy models for persistent longitudinal
health intelligence storage.

Tables:
  - health_baselines         — Personal physiological baselines
  - health_scores            — Time-series health score history
  - preventive_alerts        — Generated preventive alerts
  - health_events            — Life/health event timeline
  - symptom_logs             — Timestamped symptom entries
  - wearable_snapshots       — Raw wearable data snapshots
  - physiological_states     — Real-time inferred physiological states
  - live_sessions            — Continuous session lifecycle tracking

All tables are indexed on (user_id, recorded_at) for
efficient time-series querying.
"""

from sqlalchemy import (
    Column, Integer, String, Float,
    Boolean, DateTime, ForeignKey, Text, JSON,
)
from sqlalchemy.orm import relationship
from sqlalchemy.sql import func
from core.database import Base


# ── Personal Health Baselines ────────────────────────────────

class HealthBaselineLog(Base):
    """
    Snapshot of a user's calculated personal baselines.
    Recalculated periodically as new data arrives.
    """
    __tablename__ = "health_baselines"

    id = Column(Integer, primary_key=True, index=True)
    user_id = Column(
        Integer,
        ForeignKey("users.id", ondelete="CASCADE"),
        nullable=False,
        index=True,
    )

    # Physiological baselines
    resting_hr_bpm = Column(Float, nullable=True)
    avg_spo2_percent = Column(Float, nullable=True)
    avg_sleep_hours = Column(Float, nullable=True)
    avg_steps = Column(Float, nullable=True)
    avg_stress_level = Column(Float, nullable=True)
    avg_active_minutes = Column(Float, nullable=True)
    avg_calories_burned = Column(Float, nullable=True)
    avg_systolic_bp = Column(Float, nullable=True)
    avg_diastolic_bp = Column(Float, nullable=True)
    avg_temperature_celsius = Column(Float, nullable=True)
    avg_respiratory_rate = Column(Float, nullable=True)
    avg_blood_glucose = Column(Float, nullable=True)

    # Standard deviations (for anomaly detection)
    std_hr_bpm = Column(Float, nullable=True)
    std_spo2_percent = Column(Float, nullable=True)
    std_sleep_hours = Column(Float, nullable=True)
    std_steps = Column(Float, nullable=True)
    std_stress_level = Column(Float, nullable=True)

    # Baseline metadata
    data_points_used = Column(Integer, default=0)
    window_days = Column(Integer, default=14)
    baseline_confidence = Column(
        Float, default=0.0,
        comment="0.0–1.0: how reliable this baseline is",
    )
    freshness_score = Column(
        Float, default=0.0,
        comment="0.0–1.0: how recent the underlying data is",
    )

    recorded_at = Column(
        DateTime(timezone=True),
        server_default=func.now(),
        nullable=False,
        index=True,
    )

    user = relationship("User", backref="health_baselines")


# ── Health Score History ─────────────────────────────────────

class HealthScoreLog(Base):
    """
    Time-series log of computed health scores.
    One entry per scoring computation.
    """
    __tablename__ = "health_scores"

    id = Column(Integer, primary_key=True, index=True)
    user_id = Column(
        Integer,
        ForeignKey("users.id", ondelete="CASCADE"),
        nullable=False,
        index=True,
    )

    # Core scores (0–100)
    health_score = Column(Float, nullable=False, default=50.0)
    recovery_score = Column(Float, nullable=True)
    wellness_index = Column(Float, nullable=True)
    fatigue_score = Column(Float, nullable=True)
    cardiovascular_score = Column(Float, nullable=True)
    resilience_score = Column(Float, nullable=True)

    # Recovery modelling
    recovery_velocity = Column(
        Float, nullable=True,
        comment="Rate of recovery (higher = faster bounce-back)",
    )
    sleep_debt_hours = Column(Float, nullable=True)

    # Score metadata
    inputs_used = Column(
        JSON, nullable=True,
        comment="Which data sources contributed to this score",
    )
    freshness_score = Column(Float, default=0.0)
    score_confidence = Column(Float, default=0.0)

    recorded_at = Column(
        DateTime(timezone=True),
        server_default=func.now(),
        nullable=False,
        index=True,
    )

    user = relationship("User", backref="health_scores")


