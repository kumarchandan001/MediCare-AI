# ── Physiological State Logs (real-time) ─────────────────────

class PhysiologicalStateLog(Base):
    """
    Persists real-time inferred physiological states.

    State types: resting, active, stressed, recovering,
    sleeping, fatigued, abnormal, workout, unknown.

    Supports future digital physiology modelling and
    adaptive behavioral learning.
    """
    __tablename__ = "physiological_states"

    id = Column(Integer, primary_key=True, index=True)
    user_id = Column(
        Integer,
        ForeignKey("users.id", ondelete="CASCADE"),
        nullable=False,
        index=True,
    )

    state = Column(
        String(30), nullable=False,
        comment=(
            "resting | active | stressed | recovering | "
            "sleeping | fatigued | abnormal | workout | unknown"
        ),
    )
    confidence = Column(
        Float, default=0.0,
        comment="0.0–1.0: inference confidence",
    )
    contributing_signals = Column(
        JSON, nullable=True,
        comment="Which signals drove this state inference",
    )
    context = Column(
        JSON, nullable=True,
        comment="Contextual metadata (circadian phase, activity, etc.)",
    )
    fusion_snapshot = Column(
        JSON, nullable=True,
        comment="Fused sensor values at time of inference",
    )

    recorded_at = Column(
        DateTime(timezone=True),
        server_default=func.now(),
        nullable=False,
        index=True,
    )

    user = relationship("User", backref="physiological_states")


# ── Live Session Logs ────────────────────────────────────────

class LiveSessionLog(Base):
    """
    Tracks continuous live sessions — workout, sleep,
    recovery, and stress episodes — with full lifecycle.

    Supports:
      - session state transitions
      - session-quality scoring
      - session summaries
    """
    __tablename__ = "live_sessions"

    id = Column(Integer, primary_key=True, index=True)
    user_id = Column(
        Integer,
        ForeignKey("users.id", ondelete="CASCADE"),
        nullable=False,
        index=True,
    )

    session_type = Column(
        String(30), nullable=False,
        comment="workout | sleep | recovery | stress | unknown",
    )
    status = Column(
        String(20), default="active",
        comment="active | paused | completed | abandoned",
    )
    quality_score = Column(
        Float, nullable=True,
        comment="0–100 session quality computed at end",
    )
    summary = Column(
        JSON, nullable=True,
        comment="Session summary with key metrics",
    )
    state_transitions = Column(
        JSON, nullable=True,
        comment="List of state transitions during session",
    )
    metadata_json = Column(
        JSON, nullable=True,
        comment="Flexible extra data",
    )

    started_at = Column(
        DateTime(timezone=True),
        server_default=func.now(),
        nullable=False,
        index=True,
    )
    ended_at = Column(
        DateTime(timezone=True),
        nullable=True,
        comment="Null while session is active",
    )
    recorded_at = Column(
        DateTime(timezone=True),
        server_default=func.now(),
        nullable=False,
    )

    user = relationship("User", backref="live_sessions")
