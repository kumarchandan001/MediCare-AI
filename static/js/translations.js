// Current language state
let currentLang = 'en';

// Resolve language first from Server config, then localStorage, then default EN
if (typeof window.SERVER_LANG !== 'undefined' && window.SERVER_LANG) {
    currentLang = window.SERVER_LANG;
    localStorage.setItem('medicare_lang', currentLang);
} else {
    currentLang = localStorage.getItem('medicare_lang') || 'en';
}

// Get translation
function t(key) {
  return (
    TRANSLATIONS[currentLang]?.[key] ||
    TRANSLATIONS['en']?.[key] ||
    null
  );
}

// Apply all translations to page
function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const translation = t(key);
    // If we have a translation and it's not the exact same key, use it
    if (translation) {
      el.innerHTML = translation; // Allow HTML embedded in translations
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    const translation = t(key);
    if (translation) {
      el.placeholder = translation;
    }
  });

  // Update html lang attribute
  document.documentElement.lang =
    currentLang === 'ta' ? 'ta' :
    currentLang === 'hi' ? 'hi' : 'en';

  // Update language switcher buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
  });

  // Re-run dynamic greeting generator to preserve generated custom text like names
  if (typeof window.setGreeting === 'function') {
    window.setGreeting();
  }
}

// Switch language function
function switchLanguage(lang) {
  if (!['en', 'ta', 'hi'].includes(lang)) {
    return;
  }
  currentLang = lang;
  localStorage.setItem('medicare_lang', lang);
  applyTranslations();

  // Show toast in new language
  const msgs = {
    en: "Language changed to English",
    ta: "மொழி தமிழுக்கு மாற்றப்பட்டது",
    hi: "भाषा हिंदी में बदली गई"
  };
  
  if (typeof showToast === 'function') {
    showToast(msgs[lang], 'success');
  }

  // Update backend if user is logged in
  fetch('/api/update_language', {
    method: 'POST',
    headers: {
        'Content-Type': 'application/json'
    },
    body: JSON.stringify({ lang: lang })
  }).catch(err => console.error('Error saving language preference:', err));
}

// Auto-apply on page load
document.addEventListener('DOMContentLoaded', applyTranslations);
